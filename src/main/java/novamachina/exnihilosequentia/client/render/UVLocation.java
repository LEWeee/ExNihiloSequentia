package novamachina.exnihilosequentia.client.render;

public class UVLocation {
    private final float u;
    private final float v;

    public UVLocation(float u, float v) {
        this.u = u;
        this.v = v;
    }

    public float getU() {
        return u;
    }

    public float getV() {
        return v;
    }
}
